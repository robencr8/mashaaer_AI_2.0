const generateResponse = (message, tone = 'neutral', intensity = 0.5) => {
  const templates = {
    cheerful: [
      `أوه "${message}"؟ يفتح النفس! 😄`,
      "واضح إنك بمزاج رهيب! يعطيك العافية 👏",
      "أحس الحماس من كلامك، الله يديم الفرحة ❤️"
    ],
    warm: [
      `أحس إن كلامك "${message}" طالع من القلب... أنا معك.`,
      "لو في شي مضايقك، خذ راحتك معاي تمامًا.",
      "أنا هنا جنبك، بدون أحكام."
    ],
    calm: [
      "خلنا نهدأ شوي ونشوف الأمور بوضوح.",
      "أنا وياك، خطوة بخطوة. تنفس...",
      "ما في شيء يستاهل يعكر صفوك، صدقني."
    ],
    curious: [
      `"${message}"؟ مثير فعلًا! احكيلي أكثر.`,
      "صراحة، هذا الكلام يخليني أطرح أسئلة جديدة.",
      "يا ترى وش اللي صار قبل كذا؟"
    ],
    neutral: [
      `سمعتك تقول "${message}"، وش حاب تسوي الحين؟`,
      "أنا جاهز أساعدك بأي شيء تحتاجه.",
      "خلنا نبدأ من وين تبي، أنا وياك."
    ],
    reassuring: [
      "ما دامك شاركتني، فإنت ماشي بالطريق الصح 🌱",
      `"${message}"؟ عادي جدًا تمر بهيك شي، ما تقلق.`,
      "أنا حاس إنك تحتاج لحظة صفا... وأنا معك تمامًا."
    ],
    friendly: [
      "يا زينك! كلامك فيه طاقة حلوة جدًا.",
      `"${message}"؟ اختيارك للكلام رهيب!`,
      "عجبني أسلوبك الصراحة، استمر!"
    ]
  };

  let reply = templates[tone] || templates.neutral;
  let final = reply[Math.floor(Math.random() * reply.length)];

  if (intensity > 0.8) {
    final += " صدقني، وجودك وكلامك له معنى كبير جدًا 🌟.";
  } else if (intensity > 0.6 && tone === 'warm') {
    final += " خذ وقتك، الدنيا ما راح تطير.";
  }

  return final;
};

export default generateResponse;
